"""
Модуль административного интерфейса для модели Payout.

Этот модуль настраивает отображение и функциональность модели Payout
в административной панели Django. Реализует кастомизацию списка объектов,
фильтрации, поиска и управления удаленными записями.

Основные функции:
- Регистрация модели Payout в админ-панели
- Настройка отображения полей в списке
- Добавление фильтров для удобной навигации
- Включение поиска по текстовым полям
- Защита системных полей от редактирования
- Восстановление удаленных заявок

Декоратор @admin.register обеспечивает регистрацию модели
с указанным классом администратора.
"""

from django.contrib import admin
from .models import Payout


@admin.register(Payout)
class PayoutAdmin(admin.ModelAdmin):
    """
    Класс администратора для модели Payout.

    Настраивает представление и управление выплатами в административном интерфейсе.
    Позволяет администраторам просматривать, фильтровать, искать и управлять
    заявками на выплату, включая восстановление удаленных записей.

    Атрибуты:
        list_display (tuple): Определяет поля, отображаемые в списке объектов.
            - id: уникальный идентификатор заявки
            - amount: сумма выплаты
            - currency: валюта выплаты
            - status: текущий статус заявки
            - deleted: флаг удаления (мягкое удаление)
            - created_at: дата создания заявки
            - updated_at: дата последнего изменения заявки

        list_filter (tuple): Поля для фильтрации в правой панели.
            - status: фильтр по статусам заявки
            - deleted: фильтр по удаленным/активным заявкам
            - currency: фильтр по валютам

        search_fields (tuple): Поля, по которым выполняется поиск.
            - recipient_details: реквизиты получателя
            - comment: комментарий к заявке

        readonly_fields (tuple): Поля только для чтения.
            - id: уникальный идентификатор заявки
            - created_at: дата создания (автоматически устанавливается)
            - updated_at: дата последнего обновления (автоматически обновляется)

        list_per_page (int): Количество объектов на странице списка.
            Устанавливает пагинацию по 50 записей для оптимальной
            производительности и удобства навигации.

        ordering (tuple): Определяет порядок сортировки по умолчанию.
            Сортировка по убыванию даты создания (новые заявки вверху списка).
            Примечание: кортеж должен содержать завершающую запятую для одного элемента.

        date_hierarchy (str): Включает иерархическую навигацию по датам.
            Добавляет навигационные ссылки по годам и месяцам вверху списка,
            основанные на поле created_at, что упрощает фильтрацию заявок по периодам.

    Особенности:
        - Поддержка мягкого удаления (soft delete) через флаг 'deleted'
        - Администраторы могут восстанавливать удаленные заявки
        - Системные поля id, created_at и updated_at защищены от редактирования
        - Поиск по ключевым текстовым полям для быстрого нахождения заявок
        - Интуитивная сортировка по времени создания (новые первыми)
    """

    list_display = (
        "id",
        "amount",
        "currency",
        "status",
        "deleted",
        "created_at",
        "updated_at",
    )
    list_filter = ("status", "deleted", "currency")
    search_fields = ("recipient_details", "comment")
    readonly_fields = ("id", "created_at", "updated_at")
    list_per_page = 50
    ordering = ("-created_at",)
    date_hierarchy = "created_at"
